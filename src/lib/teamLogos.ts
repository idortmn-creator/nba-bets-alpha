export function getTeamLogoUrl(name: string): string | null {
  if (!name || name === '-') return null
  const key = name.toLowerCase().trim()

  // 1. Exact match
  const id = TEAM_IDS[key]
  if (id) return `https://cdn.nba.com/logos/nba/${id}/global/L/logo.svg`

  // 2. Word-by-word fallback — handles "OKC Thunder", "LA Lakers", etc.
  const words = key.split(/[\s_-]+/)
  for (const word of words) {
    const wid = TEAM_IDS[word]
    if (wid) return `https://cdn.nba.com/logos/nba/${wid}/global/L/logo.svg`
  }

  return null
}
