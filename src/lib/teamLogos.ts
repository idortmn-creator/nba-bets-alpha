export function getTeamLogoUrl(name: string): string | null {
  if (!name || name === '-') return null
  const key = name.toLowerCase().trim()
  const abbr = ABBR[key]
  if (!abbr) return null
  return `https://a.espncdn.com/i/teamlogos/nba/500/${abbr}.png`
}
